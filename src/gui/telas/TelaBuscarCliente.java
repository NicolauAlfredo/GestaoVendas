    private void carregarTabela() {
        try {
            ClienteDAO clienteDAO = new ClienteDAO();
            ClienteTableModel ctm = (ClienteTableModel) tbClientes.getModel();
            ctm.setDados(clienteDAO.BuscarTodos());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar tabela de clientes.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

