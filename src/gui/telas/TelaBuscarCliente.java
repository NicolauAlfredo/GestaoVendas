    private void txtNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyReleased
        TableRowSorter rs = (TableRowSorter) tbClientes.getRowSorter();
        rs.setRowFilter(RowFilter.regexFilter("(?i)" + txtNome.getText(), 0, 1));
    }//GEN-LAST:event_txtNomeKeyReleased

    private void tbClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbClientesMouseClicked
        if (evt.getClickCount() == 2) {
            ClienteTableModel ctm = (ClienteTableModel) tbClientes.getModel();
            int linhaSelecionada = tbClientes.getRowSorter().convertRowIndexToModel(tbClientes.getSelectedRow());
            Cliente cliente = ctm.getRowValue(linhaSelecionada);

            if (parent instanceof TelaLancamentoVenda) {
                TelaLancamentoVenda lv = (TelaLancamentoVenda) parent;
                lv.setCliente(cliente);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Campo de destino inv√°lido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            }

        }
    }//GEN-LAST:event_tbClientesMouseClicked

